"""
Simple CLI greeting script.

This module provides a basic command-line application that prints a
greeting message to the console. It serves as a simple entry point
for the python-hello project.
"""


def main():
    """
    Print a greeting message to the console.

    This function serves as the main entry point for the CLI application.
    It outputs a simple greeting message to stdout.

    Returns:
        None
    """
    print("Hello from python-hello!")


if __name__ == "__main__":
    # Entry point: Execute the main function when script is run directly
    main()
